let str = "Hello, I am new to JavaScript!";

console.log(str.replace("new", "excited")); // Replaces "new" with "excited"

console.log(str.length); // Outputs the length of the string

console.log(str.toUpperCase()); // Converts the string to uppercase

console.log(str.toLowerCase()); // Converts the string to lowercase

console.log(str.indexOf("new")); // Finds the index of the substring "new"

console.log(str.lastIndexOf("JavaScript")); // Finds the last index of the substring "JavaScript"

console.log(str.includes("Hello")); // Checks if the string contains "Hello"

console.log(str.split(" ")); // Splits the string into an array of words

console.log(str.trim()); // Removes whitespace from both ends of the string

console.log(str.charAt(5)); // Gets the character at index 5